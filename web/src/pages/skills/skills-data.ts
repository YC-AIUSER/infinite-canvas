// 「在画布中使用」的生成流程模板配置;仅「可直接运行」技能配置此字段
export interface SkillFlow {
    mode: "text" | "image" | "video" | "audio"; // 生成模式(决定配置节点类型)
    requiresReferenceImage?: boolean; // 生产红线:参考图为空禁止触发生成(生图技能必须为 true)
    referencePlaceholderTitle?: string; // 参考图占位节点标题(默认「参考图(必填)」)
}

export interface SkillCard {
    id: string;
    name: string; // 技能名
    zone: SkillZone; // 分区
    stage: string; // 阶段(卡片标题格式:[阶段] 技能名)
    author: string; // 作者
    problem: string; // 解决的问题(卡片两行描述)
    inputs: string[]; // 输入
    outputs: string[]; // 输出
    refAssets: string[]; // 必需参考资产
    steps?: string[]; // 执行步骤
    forbidden: string[]; // 禁止事项
    quality?: string[]; // 质量标准
    upstream: string[]; // 上游技能 id
    downstream: string[]; // 下游技能 id
    status: SkillStatus;
    usageCount?: number; // 使用次数(v1 手填)
    cover?: string; // 竖版封面图路径
    gradient?: string; // 卡片渐变色(tailwind 类或 css gradient)
    prompt?: string; // 可复制的提示词模板文本
    flow?: SkillFlow; // 「在画布中使用」流程模板;undefined = 不支持画布落地,只能复制提示词
}

